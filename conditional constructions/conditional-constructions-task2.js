var n = parseInt(prompt('Введіть ціле число'));

if (n > 0 && n % 2 == 0) {
    alert('додатне парне число');
}

else if (n > 0 && n % 2 != 0) {
    alert('додатне непарне число');
}

else if (n < 0 && n % 2 == 0) {
    alert('від’ємне парне число');
}

else if (n < 0 && n % 2 != 0) {
    alert('від’ємне непарне число');
}

else {
    alert('нульове число')
}

